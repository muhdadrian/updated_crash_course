# Inserting Elements into a List:
# You can add a new element at any position in your list by using the insert() method. You do this by
# specifying the index of the new element and the value of the new item.

motorcycles = ['honda', 'yamaha', 'suzuki']

motorcycles.insert(0, 'ducati') #just type 0 and 'ducati' - it will generate automatically
print(motorcycles)