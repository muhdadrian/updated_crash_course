# Integers:
# 2 + 3 = 5
# 3 - 2 = 1
# 2 * 3 = 6
# 3 / 2 = 1.5
# 3 ** 2 = 9
# 3 ** 3 = 27
# 10 ** 6 = 1000000
# 2 + 3 * 4 = 14
# (2 + 3) * 4 = 20


# Floats:
# 0.1 + 0.1 = 0.2
# 0.2 + 0.2 = 0.4
# 2 * 0.1 = 0.2
# 2 * 0.2 = 0.4


# Integers and Floats
# Python defaults to a float in any operation that uses float, even if the output is a whole
# number:
# 4 / 2 = 2.0
# 1 + 2.0 = 3.0
# 2 * 3.0 = 6.0
# 3.0 ** 2 = 9.0
