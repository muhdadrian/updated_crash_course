# Underscores in Numbers
# you can group digits using underscores to make large numbers more readable

universe_age = 14_000_000_000
print(universe_age)

# Multiple Assignment
# you can assign values to more than one var in a single line, to shorten your programs
# you'll use this often when initializing a set of numbers

x, y, z = 0, 0, 0
print(x, y, z)

# Const
# to treat var as constant, make the name of var all capital letters.
# Python doesn't have built-in constant types

MAX_CONNECTIONS = 5000
print(MAX_CONNECTIONS)
MAX_CONNECTIONS = 1
print(MAX_CONNECTIONS)

