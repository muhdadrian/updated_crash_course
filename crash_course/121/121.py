"""
Introducing while Loops:
The for loop takes a collection of items and executes a block of code once for each item in the
collection. In contrast, the while loop runs as long as, or while, a certain condition is true.
"""

"""
The while Loop in Action:
You can use a while loop to count up through a series of numbers. For example, the following 
while loop counts from 1 to 5_strings:
"""

current_number = 1
while current_number <= 5:
    print(current_number)
    current_number += 1

