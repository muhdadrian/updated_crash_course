# for loop

students = ['sarah', 'fatih', 'fariq'] # we define a list
for student in students: # we define a for loop. This line tells Python to pull a name from the list students, and associate it with var student.
    print(student)# we tell Python to print the name that's just been assigned to student.

# Python then repeats lines 2 and 3, once for each name in the list.
# The concept of looping is important because it's one of the most common ways a computer automates repetitive tasks.
# The colon at the end of a for statement tells Python to interpret the next line as the start of a loop.

