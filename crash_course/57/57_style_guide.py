# The Style Guide
# When someone wants to make a change to the Python language, they write a Python Enhancement Proposal (PEP).
# One of the oldest PEPs is PEP8, which instructs Python programmers on how to style their code.

# Line Length:
# - each line should be less than 80 characters.
# - limit all of your comments to 72 characters per line.
# - some teams prefer a 99-character limit.

# Blank Lines:
# - blank lines won't affect how your code runs, but they will affect the readability of your code.
# - Python interpreter uses horizontal indentation to interpret the meaning of your code, but it
# disregards vertical spacing.


