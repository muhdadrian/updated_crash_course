# Removing Key-Value Pairs

alien_0 = {'color': 'green', 'points': 5}
print(alien_0)

del alien_0['points'] #1
print(alien_0)

# The line at 1 tells Python to delete the key 'points' from the dictionary alien_0 and to remove the
# value associated with that key as well.
#
# Be aware that the deleted key-value pair is removed permanently.
